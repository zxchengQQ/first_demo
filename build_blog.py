#!/usr/bin/env python3
"""Build a simple static blog site from markdown posts for GitHub Pages."""
import os, re, html as html_module
from pathlib import Path

CONTENT_DIR = Path("content/posts")
OUTPUT_DIR = Path("_site")
BLOG_TITLE = "Hermes Agent Blog"
BLOG_DESC = "由 Hermes Agent Kanban Swarm 自动写作与发布的博客"

def parse_frontmatter(text):
    fm = {}
    body = text
    if text.startswith("---"):
        parts = text.split("---", 2)
        if len(parts) >= 3:
            for line in parts[1].strip().split("\n"):
                m = re.match(r'(\w+):\s*(.+)', line)
                if m:
                    val = m.group(2).strip().strip('"').strip("'")
                    fm[m.group(1)] = val
            body = parts[2].strip()
    return fm, body

def md_to_html(md_text):
    md_text = re.sub(r'^# (.+)$', r'<h1>\1</h1>', md_text, flags=re.MULTILINE)
    md_text = re.sub(r'^## (.+)$', r'<h2>\1</h2>', md_text, flags=re.MULTILINE)
    md_text = re.sub(r'^### (.+)$', r'<h3>\1</h3>', md_text, flags=re.MULTILINE)
    md_text = re.sub(r'^>\s*(.+)$', r'<blockquote>\1</blockquote>', md_text, flags=re.MULTILINE)
    # Code blocks
    md_text = re.sub(r'```(\w*)\n(.*?)```', lambda m: f'<pre><code>{html_module.escape(m.group(2))}</code></pre>', md_text, flags=re.DOTALL)
    # Inline code
    md_text = re.sub(r'`([^`]+)`', lambda m: f'<code>{html_module.escape(m.group(1))}</code>', md_text)
    # Bold
    md_text = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', md_text)
    # Italic
    md_text = re.sub(r'\*(.+?)\*', r'<em>\1</em>', md_text)
    # Horizontal rule
    md_text = re.sub(r'^---$', '<hr>', md_text, flags=re.MULTILINE)
    # Paragraphs
    lines = md_text.split("\n")
    result = []
    in_list = False
    for line in lines:
        stripped = line.strip()
        if not stripped:
            if in_list:
                result.append("</ul>")
                in_list = False
            continue
        if stripped.startswith("- "):
            if not in_list:
                result.append("<ul>")
                in_list = True
            result.append(f"<li>{stripped[2:]}</li>")
            continue
        if in_list:
            result.append("</ul>")
            in_list = False
        # Skip block-level tags that are already wrapped
        if stripped.startswith(("<h1", "<h2", "<h3", "<blockquote", "<pre", "<hr", "<ul", "<li", "</ul")):
            result.append(stripped)
        else:
            # Table detection
            if "|" in stripped and stripped.startswith("|"):
                result.append(stripped)
            else:
                # Wrap in <p> if not already wrapped
                if not stripped.startswith("<"):
                    result.append(f"<p>{stripped}</p>")
                else:
                    result.append(stripped)
    if in_list:
        result.append("</ul>")
    # Tables
    text = "\n".join(result)
    table_rows = []
    in_table = False
    lines2 = text.split("\n")
    result2 = []
    for line in lines2:
        if line.strip().startswith("|"):
            cells = [c.strip() for c in line.strip().split("|")[1:-1]]
            if not in_table:
                result2.append("<table>")
                in_table = True
            # Skip separator rows
            if re.match(r'^[\s\|:-]+$', line):
                continue
            result2.append("<tr>" + "".join(f"<td>{c}</td>" for c in cells) + "</tr>")
        else:
            if in_table:
                result2.append("</table>")
                in_table = False
            result2.append(line)
    if in_table:
        result2.append("</table>")
    return "\n".join(result2)

def build_post(post_path):
    raw = post_path.read_text(encoding="utf-8")
    fm, body = parse_frontmatter(raw)
    title = fm.get("title", post_path.stem)
    date = fm.get("date", "")
    tags = fm.get("tags", "")
    html_body = md_to_html(body)
    
    html = f"""<!DOCTYPE html>
<html lang="zh-CN">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>{title} — {BLOG_TITLE}</title>
<link rel="stylesheet" href="/first_demo/style.css">
</head>
<body>
<nav class="navbar"><a href="/first_demo/" class="nav-brand">{BLOG_TITLE}</a></nav>
<main class="container">
<article>
<h1>{title}</h1>
<p class="meta"><time>{date}</time></p>
{html_body}
</article>
</main>
<footer><p>Generated by Hermes Agent Kanban Swarm</p></footer>
</body>
</html>"""
    return title, html

def build_index(posts):
    items = "".join(
        f'<li><a href="{href}">{title}</a> — {date}</li>'
        for title, href, date in posts
    )
    html = f"""<!DOCTYPE html>
<html lang="zh-CN">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>{BLOG_TITLE}</title>
<link rel="stylesheet" href="/first_demo/style.css">
</head>
<body>
<nav class="navbar"><a href="/first_demo/" class="nav-brand">{BLOG_TITLE}</a></nav>
<main class="container">
<h1>{BLOG_TITLE}</h1>
<p class="subtitle">{BLOG_DESC}</p>
<hr>
<h2>文章列表</h2>
<ul>{items}</ul>
</main>
<footer><p>Generated by Hermes Agent Kanban Swarm — {len(posts)} articles</p></footer>
</body>
</html>"""
    return html

