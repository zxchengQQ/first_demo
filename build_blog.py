def main():
    posts_dir = CONTENT_DIR
    if not posts_dir.exists():
        print(f"Content directory {posts_dir} not found.")
        return
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    md_files = sorted(posts_dir.glob("*.md"))
    post_list = []
    for f in md_files:
        title, html = build_post(f)
        out_name = f.stem + ".html"
        (OUTPUT_DIR / out_name).write_text(html, encoding="utf-8")
        print(f"  - {out_name} ({title})")
        post_list.append((title, out_name, ""))
    # Generate index
    index_html = build_index(post_list)
    (OUTPUT_DIR / "index.html").write_text(index_html, encoding="utf-8")
    # Generate CSS
    (OUTPUT_DIR / "style.css").write_text(build_css(), encoding="utf-8")
    # Copy content/ structure
    content_out = OUTPUT_DIR / "content" / "posts"
    content_out.mkdir(parents=True, exist_ok=True)
    for f in md_files:
        content = f.read_text(encoding="utf-8")
        (content_out / f.name).write_text(content, encoding="utf-8")
    print(f"\nBuilt {len(md_files)} posts to {OUTPUT_DIR.resolve()}")

if __name__ == "__main__":
    main()
